def add(x,y):
    return x+y

def sub(x,y):
    return x-y

def mul(x,y):
    return x*y

def divide(x,y):
    return x/y

num1 = int(input("Enter a number:"))
num2 = int(input("Enter another number:"))


print(add(num1,num2))

print(sub(num1,num2))

print(mul(num1,num2))

print(divide(num1,num2))